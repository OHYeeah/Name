def summe(a, b):
    c = a + b
    return c

def differenz(a, b):
    c = a - b
    return c

a = 2
b = 1

ergebnis_summe = summe(a, b)
ergebnis_differenz = differenz(a, b)

print("Summe ", a, "und", b, "ist:", ergebnis_summe)
print("Differenz ", a, "und", b, "ist:", ergebnis_differenz)
